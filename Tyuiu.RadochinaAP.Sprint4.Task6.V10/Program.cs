            Console.WriteLine("**********************************************************************************************************************************************");
            Console.WriteLine("****************************************************************************************************************************************");
            Console.WriteLine("* РЕЗУЛЬТАТ:                                                                                                                                *");
            Console.WriteLine("********************************************************************************************************************************************");

            string[] res = ds.Calculate(array);

            Console.WriteLine("Элементы массива с длиной меньше 7 символов:");
            if (res.Length > 0)
            {
                for (int i = 0; i < res.Length; i++)
                {
                    Console.WriteLine($"[{i}] = \"{res[i]}\"");
                }
            }
            else
            {
                Console.WriteLine("Нет элементов с длиной меньше 7 символов");
            }

            Console.ReadKey();
        }
