        static void Main(string[] args)
        {

            DataService ds = new DataService();

            Console.WriteLine("**********************************************************************************************************************************");
            Console.WriteLine("* Спринт #4                                                                                                                      *");
            Console.WriteLine("* Тема: Двумерные массивы (генератор случайных чисел)                                                                                            *");
            Console.WriteLine("* Задание #6                                                                                                                     *");
            Console.WriteLine("* Вариант #6                                                                                                                     *");
            Console.WriteLine("* Выполнила: Радочина Арина Павловна | ПИНб-25-1                                                                                 *");
            Console.WriteLine("**********************************************************************************************************************************");
            Console.WriteLine("* УСЛОВИЕ:                                                                                                                       *");
            Console.WriteLine("* Дан строковый массив данных [\"Театр\", \"Кино\", \"Музей\", \"Парк\", \"Зоопарк\", \"Концерт\", \"Выставка\"],                *");
            Console.WriteLine("* используя класс Array, выведите элементы массива, длина которых меньше 7 символов.                                                                                     *");
